function [fitresult, gof] = Fitting_Plot(U, I, W)
%CREATEFIT(U,I,W)
%  Create a fit.
%
%  Data for 'Fitting Result' fit:
%      X Input : U
%      Y Output: I
%      Weights : W
%  Output:
%      fitresult : a fit object representing the fit.
%      gof : structure with goodness-of fit info.
%
%  See also FIT, CFIT, SFIT.

%  Auto-generated by MATLAB on 30-May-2021 13:51:43
%  == edited later ==


%% Fit: 'Fitting Result'.
[xData, yData, weights] = prepareCurveData( U, I, W );

% Set up fittype and options.
ft = fittype( 'exp2' );
opts = fitoptions( 'Method', 'NonlinearLeastSquares' );
opts.Display = 'Off';
opts.StartPoint = [6.25591080244463e-08 30.8578574736571 0 30.8578574736571];
opts.Weights = weights;

% Fit model to data.
[fitresult, gof] = fit( xData, yData, ft, opts );

% Plot fit with data.
figure( 'Name', 'Diode Forward Volt-Ampere Characteristic' );
h = plot( fitresult, xData, yData, 'x' );
axis( [0 0.55 0 2] );
title( 'Diode Forward Volt-Ampere Characteristic' );
legend( h, 'Data Point', 'Fitting Result', 'Location', 'NorthWest', 'Interpreter', 'none' );
% Label axes
xlabel( 'U(V)', 'Interpreter', 'none' );
ylabel( 'I(mA)', 'Interpreter', 'none' );
grid on


